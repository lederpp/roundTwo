# Look Over

import turtle

turtle.penup()
turtle.goto(50, 50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.goto(-50, 50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.goto(-50, -50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.goto(50, -50)
turtle.pendown()
turtle.circle(50)

turtle.hideturtle()
turtle.done()